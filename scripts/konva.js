var width = window.innerWidth;
var height = window.innerHeight - 200;

function updateTooltip(tooltip, x, y, text) {
  tooltip.getText().text(text);
  tooltip.position({
    x: x,
    y: y,
  });
  tooltip.show();
}

var stage = new Konva.Stage({
  container: 'container',
  width: width,
  height: height,
});

var shapesLayer = new Konva.Layer();
var tooltipLayer = new Konva.Layer();

var tooltip = new Konva.Label({
  opacity: 1,
  visible: false,
  listening: false,
});
tooltip.add(
  new Konva.Tag({
    fill: 'black',
    pointerDirection: 'down',
    pointerWidth: 10,
    pointerHeight: 10,
    lineJoin: 'round',
    cornerRadius: 3,
  })
);
tooltip.add(
  new Konva.Text({
    text: '',
    align: 'center',
    fontFamily: 'Calibri',
    fontSize: 18,
    padding: 5,
    fill: 'white',
  })
);
tooltipLayer.add(tooltip);

// get areas data
document.addEventListener("DOMContentLoaded", () => {
  init();
});

document.querySelector('#selectItems').addEventListener('change', (event) => {
  //stage.destroyChildren();
  shapesLayer.destroy();
  init();
});

function init() {
  var item = document.getElementById("selectItems").value;
  var areas = {}

  switch (item) {
    case 'bryophyta':
      areas = bryophytaPoints();
      break;
    case 'dumortiera_hirsuta':
      areas = dumortieraHirsutaPoints();
      break;
    case 'equisetum':
      areas = equisetumPoints();
      break;
    case 'marchantia_polymorpha':
      areas = marchantiaPolymorphaPointsPoints()
      break;
    case 'phaoceros_carolinianus':
      areas = phaocerosCarolinianusPoints();
      break;
    case 'polystichum_munitum':
      areas = polystichumMunitumPoints();
      break;
    case 'psilotum_nudum':
      areas = psilotumNudumPoints();
      break;
    case 'selaginella':
      areas = selaginellaPoints();
      break;
    case 'corumbellaemcalcario':
      areas = corumbellaemcalcarioPoints()
      break;
    case 'smilacaceaeemcalcario':
      areas = smilacaceaeemcalcarioPoints()
      break;
    case 'lacusovagusmagnificens':
      areas = lacusovagusmagnificensPoints()
      break;
  }

  // draw areas
  for (var key in areas) {
    var area = areas[key];
    var points = area.points;

    var shape = new Konva.Line({
      points: points,
      fill: area.color,
      opacity: 0,
      closed: true,
      // custom attr
      key: key,
    });
    shapesLayer.add(shape);
  }

  stage.add(shapesLayer);
  stage.add(tooltipLayer);
}

stage.on('mouseover', (evt) => {
  var shape = evt.target;
  if (shape) {
    shape.opacity(0.6);
  }
});
stage.on('mouseout', (evt) => {
  var shape = evt.target;
  if (shape) {
    shape.opacity(0);
    tooltip.hide();
  }
});
stage.on('mousemove', (evt) => {
  var shape = evt.target;
  if (shape) {
    var mousePos = stage.getPointerPosition();
    var x = mousePos.x;
    var y = mousePos.y - 5;
    updateTooltip(tooltip, x, y, shape.attrs.key);
  }
});